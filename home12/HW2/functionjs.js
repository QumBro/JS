const obj = {
    a: '1',
    b: '2',
    c: '3',
    e: '4',
    o: '5'
}

const vowels = new Array('a', 'e', 'i', 'o', 'u');

function isVowel() {
    for (key in obj) {
        if (key === vowels.forEach(function () {
            Object.defineProperty(obj, key, {
                writable: false
            });
        }));
    }
}

isVowel();